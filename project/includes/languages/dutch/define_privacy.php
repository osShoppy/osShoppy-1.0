<strong>Wet bescherming persoonsgegevens (WBP)</strong><br>
Bij het verwerken van persoonsgegevens wordt de Wet Bescherming Persoonsgegevens (WBP) gevolgd. 
Deze wet helpt de privacy van burgers te beschermen. De WBP is onder andere van toepassing op gegevens 
die verwerkt worden via websites. Dit betekent dat ook persoonsgegevens die via internet worden verzameld, 
bewaard en gebruikt, onder deze wet vallen.<br><br>

